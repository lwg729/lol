package com.pojo;

public class Lol {
    private int hid;
    private String hero;
    private int hspeed;
    private int hpower;
    private int hblood;

    public int getHid() {
        return hid;
    }

    public void setHid(int hid) {
        this.hid = hid;
    }

    public String getHero() {
        return hero;
    }

    public void setHero(String hero) {
        this.hero = hero;
    }

    public int getHspeed() {
        return hspeed;
    }

    public void setHspeed(int hspeed) {
        this.hspeed = hspeed;
    }

    public int getHpower() {
        return hpower;
    }

    public void setHpower(int hpower) {
        this.hpower = hpower;
    }

    public int getHblood() {
        return hblood;
    }

    public void setHblood(int hblood) {
        this.hblood = hblood;
    }


}
